/*
 * Pick a suitable Mac encoding value for a Unicode string.
 *
 * This routine is only used during file renaming.
 */
u_int32_t
hfs_pickencoding(const u_int16_t *src, int len)
{
	u_int32_t guess;
	u_int16_t ch;
	u_int8_t bits;
	u_int8_t cjkstate;

	cjkstate = 0;
	guess = kTextEncodingMacRoman;

	while (len--) {
		ch = *src++;
		if (ch < 0x0080)  /* ASCII */
			continue;

		if (ch >= 0x4E00 && ch <= 0x9FAF) {  /* CJK */
			bits = cjk_bitmap[(ch - 0x4E00) >> 1];
			if (ch & 1)
				bits = bits >> 4;
			bits &= 0x0F;
			if (bits) {
				if (cjkstate) {
					bits &= cjkstate;
					if (bits)
						cjkstate = bits;
				} else
					cjkstate = bits;
			}
			continue;
		}
		if (ch >= 0x3041 && ch <= 0x30FE) {  /* Hiragana & Katakana */
			if (cjkstate) {
				bits = cjkstate & CJK_KATAKANA;
				if (bits)
					cjkstate = bits;
			} else
				cjkstate = CJK_KATAKANA;
			continue;
		}
		if ((ch >= 0x1100 && ch <= 0x11F9) ||  /* Hangul Jamo */
		    (ch >= 0x3131 && ch <= 0x318E) ||  /* Hangul Compatibility Jamo */
		    (ch >= 0xF900 && ch <= 0xFA0B)) {  /* CJK Compatibility Ideographs */	      
			cjk_lastunique = CJK_KOREAN;
			return kTextEncodingMacKorean;
		}
		if (ch >= 0x3105 && ch <= 0x3129) {  /* Bopomofo */
			if (cjkstate) {
				bits = cjkstate & CJK_CHINESE;
				if (bits)
					cjkstate = bits;
			} else
				cjkstate = CJK_CHINESE;
			continue;
		}
		if (ch >= 0xFF01 && ch <= 0xFFE6) {  /* Halfwidth and Fullwidth */
			if (cjkstate == 0)
				cjkstate = CJK_ALL;
			continue;
		}
		if (hfs_islatinbias && ch >= 0x0300 && ch <= 0x0329) {
			guess = hfs_encodingbias;
			continue;
		}
		if (ch <= 0x03CE && ch >= 0x0384) {
			guess = kTextEncodingMacGreek;
			continue;
		}
		if (ch <= 0x0491 && ch >= 0x0401) {
			guess = kTextEncodingMacCyrillic;
			continue;
		}
		if (ch >= 0x05B0 && ch <= 0x05EA) {
			return kTextEncodingMacHebrew;
		}
		if (ch >= 0x060C && ch <= 0x06d5) {
			return kTextEncodingMacArabic;
		}
		if (ch >= 0x0E00 && ch <= 0x0E5B) {
			return kTextEncodingMacThai;
		}
		/* Catch a few Shift-JIS strays */
		if (guess == 0 || guess == kTextEncodingMacUnicode) {
			if (ch == 0x2010 || ch == 0x2014 || ch == 0x2015 || ch == 0x2016) {
				guess = kTextEncodingMacJapanese;
				if ((cjkstate == 0) || (cjkstate & CJK_JAPAN))
					cjkstate = CJK_JAPAN;
				else
					cjkstate |= CJK_JAPAN;
				continue;
			}
			if ((hfs_encodingbias == kTextEncodingMacJapanese) &&
			    (ch == 0x00A2 || ch == 0x00A3 || ch == 0x00AC)) {
				guess = kTextEncodingMacJapanese;
				continue;
			}
			/* TM char depends on the Mac encoding used. */
			if (ch == 0x2122) {
				switch(hfs_encodingbias) {
				case kTextEncodingMacJapanese:
				case kTextEncodingMacChineseTrad:
				case kTextEncodingMacKorean:
				case kTextEncodingMacGreek:
				case kTextEncodingMacThai:
				case kTextEncodingMacChineseSimp:
					guess = hfs_encodingbias;
					break;
				}
			}
		}
		if (guess == 0 && ch > 0x2122) {
			guess = kTextEncodingMacUnicode;
		}
	} /* end while */

	if (cjkstate) {
		if (powerof2(cjkstate)) {
			cjk_lastunique = cjkstate;
			return ((u_int32_t)cjk_encoding[cjkstate]);
		} 
		if (hfs_encodingbias != 0) {
			switch(hfs_encodingbias) {
			case kTextEncodingMacJapanese:
				if (cjkstate & CJK_JAPAN)
					return (kTextEncodingMacJapanese);
				break;
			case kTextEncodingMacKorean:
				if (cjkstate & CJK_KOREAN)
					return (kTextEncodingMacKorean);
				break;
			case kTextEncodingMacChineseTrad:
				if (cjkstate & CJK_CHINESE_TRAD)
					return (kTextEncodingMacChineseTrad);
				if (cjkstate & CJK_CHINESE_SIMP)
					return (kTextEncodingMacChineseSimp);
				break;
			case kTextEncodingMacChineseSimp:
				if (cjkstate & CJK_CHINESE_SIMP)
					return (kTextEncodingMacChineseSimp);
				if (cjkstate & CJK_CHINESE_TRAD)
					return (kTextEncodingMacChineseTrad);
				break;
			}
		}
		if (cjk_lastunique) {
			if (cjkstate & cjk_lastunique)
				cjkstate = cjk_lastunique;
			else
				cjk_lastunique = 0;
		}
		return ((u_int32_t)cjk_encoding[cjkstate]);
	}
	
	return guess;
}

